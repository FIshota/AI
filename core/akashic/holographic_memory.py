@dataclass
class MemoryHologram:
    """
    A single memory encoded holographically.

    Each memory carries not only its own content but the echoes of how that
    content resonates across all domains. When retrieved, those echoes
    activate cross-domain associations automatically.

    The holographic metaphor: in a physical hologram, every region of the
    plate contains information about the whole scene. Here, every memory
    contains projections of itself into every other domain — the whole
    is encoded in each part.
    """
    content: str
    domain: str
    timestamp: float = field(default_factory=time.time)
    # domain → echo description
    cross_domain_echoes: dict[str, str] = field(default_factory=dict)
    # Degree of interference with the existing memory field (0 = isolated, 1 = maximally entangled)
    interference_score: float = 0.0
    # Estimated integrated information of this memory
    phi: float = 0.0
    # Stable hash identifier
    uid: str = ""

    def __post_init__(self) -> None:
        if not self.uid:
            self.uid = self._compute_uid()

    def _compute_uid(self) -> str:
        payload = f"{self.content[:200]}|{self.domain}|{self.timestamp:.3f}"
        return hashlib.sha256(payload.encode()).hexdigest()[:16]

    def echo_for(self, domain: str) -> str:
        """Return the cross-domain echo for a given domain, or a default."""
        if domain == self.domain:
            return self.content
        return self.cross_domain_echoes.get(
            domain,
            f"No direct echo of '{self.domain}' memory in '{domain}' domain.",
        )

    def to_dict(self) -> dict:
        return asdict(self)

    @classmethod
    def from_dict(cls, data: dict) -> MemoryHologram:
        return cls(**data)

    def __repr__(self) -> str:
        preview = self.content[:60].replace("\n", " ")
        return (
            f"MemoryHologram(uid={self.uid}, domain={self.domain!r}, "
            f"Φ={self.phi:.3f}, content={preview!r}…)"
        )


@dataclass
class InterferencePattern:
    """
    The interference pattern between two memories.

    In wave optics, two waves can interfere constructively (amplify each other)
    or destructively (cancel each other). Here, two memories interfere based on
    whether their cross-domain echoes align (constructive) or contradict
    (destructive).

    - constructive: the memories reinforce each other, revealing a shared
      deep structure (synthesis is richer than either alone)
    - destructive: the memories tension each other, revealing a genuine
      contradiction or complementary perspective
    - neutral: the memories are largely orthogonal — different domains with
      little overlap
    """
    type: str  # "constructive" | "destructive" | "neutral"
    amplification: float  # > 1 constructive, < 1 destructive, ≈ 1 neutral
    synthesis: str  # natural-language description of the interference

    def __repr__(self) -> str:
        return (
            f"InterferencePattern(type={self.type!r}, "
            f"amplification={self.amplification:.3f}, "
            f"synthesis={self.synthesis[:80]!r}…)"
        )


