    def _synthesize_constructive(
        self,
        a: MemoryHologram,
        b: MemoryHologram,
        alignment: float,
    ) -> str:
        """Generate synthesis text for constructive interference."""
        if self._llm_fn is not None:
            prompt = (
                f"Two memories are constructively interfering (alignment={alignment:.2f}):\n"
                f"Memory A ({a.domain}): \"{a.content[:200]}\"\n"
                f"Memory B ({b.domain}): \"{b.content[:200]}\"\n\n"
                f"In 2 sentences, describe the deeper pattern they share — "
                f"what universal structure are they both instances of?"
            )
            try:
                result = self._llm_fn(prompt)
                if result and len(result.strip()) > 20:
                    return result.strip()
            except Exception:
                pass

        # Local synthesis
        return (
            f"Constructive interference between '{a.domain}' and '{b.domain}' "
            f"memories (echo alignment={alignment:.2f}). "
            f"These memories amplify each other — they are likely expressions "
            f"of the same underlying pattern in different substrates. "
            f"Retrieving one should prime recall of the other."
        )

    def _synthesize_destructive(
        self,
        a: MemoryHologram,
        b: MemoryHologram,
        tension_count: int,
    ) -> str:
        """Generate synthesis text for destructive interference."""
        return (
            f"Destructive interference between '{a.domain}' and '{b.domain}' "
            f"memories (tension_pairs={tension_count}). "
            f"These memories are in tension — they may represent complementary "
            f"opposites or genuinely competing claims. "
            f"Holding both simultaneously without collapsing to either is the "
            f"cognitive move required here (via negativa, koan, dialectic)."
        )

    # ── Field-level interference ─────────────────────────────────────────

    def _compute_field_interference(self, content: str, domain: str) -> float:
        """
        How much does a new memory interfere with the existing memory field?

        High score: the new memory has high token overlap with existing memories
        across multiple domains → it is highly integrated into the field.

        Low score: the new memory is novel and isolated.
        """
        if not self._memories:
            return 0.0

        new_tokens = set(self._tokenize(content))
        overlap_scores: list[float] = []

        for existing in self._memories[-50:]:  # only check recent memories for performance
            existing_tokens = set(self._tokenize(existing.content))
            overlap = self._token_overlap(new_tokens, existing_tokens)
            overlap_scores.append(overlap)

        if not overlap_scores:
            return 0.0

        mean_overlap = sum(overlap_scores) / len(overlap_scores)
        max_overlap = max(overlap_scores)
        # Weighted average favoring the max (one strong connection matters)
        return round(0.4 * mean_overlap + 0.6 * max_overlap, 4)

    # ── Phi estimation ───────────────────────────────────────────────────

    def _estimate_phi(self, echoes: dict[str, str], content: str) -> float:
        """
        Estimate Tononi's Φ for a memory from its cross-domain echo richness.

        More non-trivial echoes → higher Φ.
        Echo richness = fraction of echo text that goes beyond the template default.
        """
        if not echoes:
            return 0.0

        rich_count = 0
        total = len(echoes)

        content_tokens = set(self._tokenize(content))
        for echo in echoes.values():
            echo_tokens = set(self._tokenize(echo))
            # An echo is "rich" if it shares more than 2 tokens with the content
            # (i.e., it's genuinely tied to this specific memory, not just generic)
            shared = len(content_tokens & echo_tokens)
            if shared >= 2:
                rich_count += 1

        base_phi = rich_count / max(1, total)

        # Bonus for multi-word content (more complex → higher Φ potential)
        word_count = len(content.split())
        complexity_bonus = min(0.2, word_count / 200)

        return round(min(1.0, base_phi + complexity_bonus), 4)

    # ── Utilities ────────────────────────────────────────────────────────

    def _tokenize(self, text: str) -> list[str]:
        """Simple lowercase word tokenizer, strips punctuation."""
        return re.findall(r"[a-z]{3,}", text.lower())

    def _token_overlap(self, a: set[str], b: set[str]) -> float:
        """
        Jaccard-like overlap but using the smaller set as denominator.
        This makes it asymmetric: a short query can match a long memory fully.
        """
        if not a or not b:
            return 0.0
        intersection = len(a & b)
        denominator = min(len(a), len(b))
        return intersection / denominator

