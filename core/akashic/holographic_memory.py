class HolographicMemory:
    """
    ホログラフィック記憶システム

    Stores memories as holograms: each encoded memory carries projections
    of itself into every other domain. Recall is not a lookup but a resonance
    activation — querying for one concept simultaneously activates its
    cross-domain echoes.

    Architecture
    ────────────
    Memory storage:   list of MemoryHologram, indexed by domain
    Retrieval:        cosine-like token overlap between query and all memories
    Interference:     pairwise echo alignment score
    Boundary:         holographic summary encoding the whole collection
    Persistence:      JSON at data/akashic_memory.json

    Usage::

        mem = HolographicMemory()
        h = mem.encode("recursion is when a function calls itself", "code")
        results = mem.recall("self-reference in programs", domains=["code", "mathematics"])
        pattern = mem.find_interference(results[0], results[1])
        boundary = mem.get_boundary_encoding(results)
    """

    def __init__(
        self,
        storage_path: str = _DEFAULT_MEMORY_PATH,
        llm_fn: Callable[[str], str] | None = None,
    ) -> None:
        self._path = os.path.abspath(storage_path)
        self._llm_fn = llm_fn
        self._memories: list[MemoryHologram] = []
        self._load()

    # ── Public API ──────────────────────────────────────────────────────

    def encode(
        self,
        content: str,
        domain: str,
        context: dict | None = None,
    ) -> MemoryHologram:
        """
        Encode a new memory as a hologram with cross-domain projections.

        The encoding process:
        1. Project the content into all other domains using template echoes
           (and LLM if available)
        2. Compute the memory's interference score against the existing field
        3. Estimate Φ from cross-domain activation density
        4. Persist the new hologram

        Parameters
        ──────────
        content:  The raw content to remember
        domain:   The primary domain of this memory (e.g. "code", "biology")
        context:  Optional metadata dict (tags, source, conversation_id, etc.)

        Returns the fully encoded MemoryHologram.
        """
        if domain not in _ALL_DOMAINS:
            domain = self._nearest_domain(domain)

        echoes = self._project_cross_domain(content, domain, context)
        phi = self._estimate_phi(echoes, content)
        interference = self._compute_field_interference(content, domain)

        hologram = MemoryHologram(
            content=content,
            domain=domain,
            cross_domain_echoes=echoes,
            phi=phi,
            interference_score=interference,
        )

        self._memories.append(hologram)
        self._save()
        return hologram

    def recall(
        self,
        query: str,
        domains: list[str] | None = None,
        top_k: int = 5,
    ) -> list[MemoryHologram]:
        """
        Retrieve memories that resonate with the query.

        Unlike a vector database lookup, recall here activates cross-domain
        associations: a query about "recursion" will surface not only code
        memories but biology, philosophy, and mathematics memories whose
        cross-domain echoes mention recursion.

        Parameters
        ──────────
        query:    Natural language query
        domains:  Restrict search to these domains (None = all domains)
        top_k:    Return at most this many results

        Returns memories sorted by resonance score, highest first.
        """
        if not self._memories:
            return []

        query_tokens = set(self._tokenize(query))
        candidates = self._memories

        if domains:
            # Include memories in target domains AND memories whose echoes
            # for target domains are relevant
            candidates = [
                m for m in self._memories
                if m.domain in domains
                or any(
                    d in domains and self._token_overlap(
                        query_tokens, set(self._tokenize(m.echo_for(d)))
                    ) > 0
                    for d in (domains or _ALL_DOMAINS)
                )
            ]

        scored: list[tuple[float, MemoryHologram]] = []
        for mem in candidates:
            score = self._resonance_score(query_tokens, mem, domains)
            if score > 0:
                scored.append((score, mem))

        scored.sort(key=lambda x: x[0], reverse=True)
        return [mem for _, mem in scored[:top_k]]

    def find_interference(
        self,
        memory_a: MemoryHologram,
        memory_b: MemoryHologram,
    ) -> InterferencePattern:
        """
        Find the interference pattern between two memories.

        Constructive interference: their cross-domain echoes align and amplify
        each other — they share a deep structure.

        Destructive interference: their echoes are in tension — they represent
        genuinely competing perspectives or complementary opposites.

        Neutral: they are largely orthogonal — different domains, different
        content, no significant overlap or tension.
        """
        alignment_scores: list[float] = []
        shared_domains = set(memory_a.cross_domain_echoes) & set(memory_b.cross_domain_echoes)

        for domain in shared_domains:
            echo_a = set(self._tokenize(memory_a.echo_for(domain)))
            echo_b = set(self._tokenize(memory_b.echo_for(domain)))
            overlap = self._token_overlap(echo_a, echo_b)
            alignment_scores.append(overlap)

        if not alignment_scores:
            return InterferencePattern(
                type="neutral",
                amplification=1.0,
                synthesis=(
                    f"'{memory_a.domain}' and '{memory_b.domain}' memories "
                    f"have no shared domain echoes — they are orthogonal holograms."
                ),
            )

        mean_alignment = sum(alignment_scores) / len(alignment_scores)

        # Constructive: high shared token overlap in echoes
        if mean_alignment >= 0.25:
            amplification = 1.0 + mean_alignment * 2.0
            synthesis = self._synthesize_constructive(memory_a, memory_b, mean_alignment)
            return InterferencePattern(
                type="constructive",
                amplification=amplification,
                synthesis=synthesis,
            )

        # Check for tension words suggesting destructive interference
        tension_pairs = [
            ("order", "chaos"), ("deterministic", "random"), ("local", "global"),
            ("simple", "complex"), ("reducible", "emergent"), ("finite", "infinite"),
            ("bottom-up", "top-down"), ("discrete", "continuous"),
        ]
        content_ab = (memory_a.content + " " + memory_b.content).lower()
        tension_count = sum(
            1 for a, b in tension_pairs
            if a in content_ab and b in content_ab
        )

        if tension_count >= 2 or mean_alignment < 0.05:
            amplification = max(0.2, 1.0 - (tension_count * 0.15))
            synthesis = self._synthesize_destructive(memory_a, memory_b, tension_count)
            return InterferencePattern(
                type="destructive",
                amplification=amplification,
                synthesis=synthesis,
            )

        # Default: neutral with slight constructive lean
        synthesis = (
            f"Memories in '{memory_a.domain}' and '{memory_b.domain}' show "
            f"weak resonance (alignment={mean_alignment:.2f}). "
            f"They neither strongly reinforce nor oppose each other — "
            f"they may be exploring adjacent but distinct regions of the semantic field."
        )
        return InterferencePattern(
            type="neutral",
            amplification=1.0 + mean_alignment * 0.5,
            synthesis=synthesis,
        )

    def get_boundary_encoding(self, memories: list[MemoryHologram]) -> str:
        """
        Encode the holographic boundary summary of a collection of memories.

        Holographic principle: the information of a volume is encoded on its
        boundary surface. Applied cognitively: the essential information of a
        set of memories can be encoded in a lower-dimensional boundary summary
        that preserves the relational structure.

        The boundary encoding:
        1. Extracts the dominant domains across the collection
        2. Identifies the most active cross-domain bridges
        3. Computes the collective Φ of the set
        4. Returns a compact but information-rich summary

        This boundary can be stored as a "memory of memories" — a higher-order
        hologram that represents the collection without containing all its detail.
        """
        if not memories:
            return "∅ — empty memory collection, boundary is undefined."

        # Domain frequency
        domain_counts: dict[str, int] = {}
        for m in memories:
            domain_counts[m.domain] = domain_counts.get(m.domain, 0) + 1

        dominant_domain = max(domain_counts, key=lambda d: domain_counts[d])
        domain_summary = ", ".join(
            f"{d}×{c}" for d, c in sorted(domain_counts.items(), key=lambda x: -x[1])
        )

        # Cross-domain echo inventory: which bridge domains appear most?
        echo_domain_counts: dict[str, int] = {}
        for m in memories:
            for echo_domain in m.cross_domain_echoes:
                echo_domain_counts[echo_domain] = (
                    echo_domain_counts.get(echo_domain, 0) + 1
                )
        top_bridges = sorted(
            echo_domain_counts.items(), key=lambda x: -x[1]
        )[:3]
        bridge_summary = ", ".join(f"{d}({c})" for d, c in top_bridges)

        # Collective Φ
        phi_values = [m.phi for m in memories if m.phi > 0]
        collective_phi = sum(phi_values) / len(phi_values) if phi_values else 0.0

        # Content fingerprint: first 30 tokens from each memory, deduplicated
        all_tokens: set[str] = set()
        for m in memories:
            all_tokens.update(self._tokenize(m.content)[:15])
        stopwords = {
            "the", "a", "an", "is", "are", "was", "were", "be", "been",
            "being", "have", "has", "had", "do", "does", "did", "will",
            "would", "could", "should", "may", "might", "must", "shall",
            "to", "of", "in", "for", "on", "with", "at", "by", "from",
            "and", "or", "but", "not", "this", "that", "it", "its",
        }
        content_tokens = sorted(all_tokens - stopwords)[:20]
        token_fingerprint = " ".join(content_tokens)

        boundary = (
            f"HOLOGRAPHIC BOUNDARY ENCODING\n"
            f"━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n"
            f"Memory count:      {len(memories)}\n"
            f"Domains:           {domain_summary}\n"
            f"Primary domain:    {dominant_domain}\n"
            f"Top echo bridges:  {bridge_summary}\n"
            f"Collective Φ:      {collective_phi:.4f}\n"
            f"Content tokens:    {token_fingerprint}\n"
            f"━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n"
            f"This boundary encodes the relational structure of {len(memories)} "
            f"memories. The dominant domain '{dominant_domain}' anchors the "
            f"collection; the bridges into {bridge_summary.split('(')[0] if bridge_summary else 'unknown'} "
            f"suggest the collection's cross-domain reach. "
            f"Collective Φ={collective_phi:.3f} measures how integrated the "
            f"information across this set is."
        )
        return boundary

    def all_memories(self, domain: str | None = None) -> list[MemoryHologram]:
        """Return all stored memories, optionally filtered by domain."""
        if domain is None:
            return list(self._memories)
        return [m for m in self._memories if m.domain == domain]

    def clear(self) -> None:
        """Clear all memories and delete the persisted file."""
        self._memories = []
        if os.path.exists(self._path):
            os.remove(self._path)

    # ── Persistence ─────────────────────────────────────────────────────

    def _save(self) -> None:
        os.makedirs(os.path.dirname(self._path), exist_ok=True)
        data = [m.to_dict() for m in self._memories]
        with open(self._path, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

    def _load(self) -> None:
        if not os.path.exists(self._path):
            self._memories = []
            return
        try:
            with open(self._path, "r", encoding="utf-8") as f:
                data = json.load(f)
            self._memories = [MemoryHologram.from_dict(d) for d in data]
        except (json.JSONDecodeError, KeyError, TypeError):
            # Corrupt or incompatible file — start fresh
            self._memories = []

    # ── Cross-domain projection ──────────────────────────────────────────

    def _project_cross_domain(
        self,
        content: str,
        primary_domain: str,
        context: dict | None,
    ) -> dict[str, str]:
        """
        Generate cross-domain echoes for a piece of content.

        Strategy (degrading gracefully without LLM):
        1. Use template echoes for the primary domain
        2. Augment with content-specific nuance via LLM if available
        3. Tag echoes with context metadata if provided
        """
        echoes: dict[str, str] = {}
        domain_templates = _DOMAIN_ECHO_TEMPLATES.get(primary_domain, {})

        for target_domain, template in domain_templates.items():
            if target_domain == primary_domain:
                continue

            if self._llm_fn is not None:
                echo = self._llm_echo(content, primary_domain, target_domain, template)
            else:
                # Template echo: concrete and useful even without LLM
                echo = template

            if context:
                tags = context.get("tags", [])
                if isinstance(tags, list) and tags:
                    echo += f" [context: {', '.join(str(t) for t in tags[:3])}]"

            echoes[target_domain] = echo

        # Ensure all domains have at least a minimal echo
        for domain in _ALL_DOMAINS:
            if domain != primary_domain and domain not in echoes:
                echoes[domain] = (
                    f"Indirect echo of {primary_domain} memory in {domain} domain — "
                    f"no direct template bridge available."
                )

        return echoes

    def _llm_echo(
        self,
        content: str,
        source_domain: str,
        target_domain: str,
        template: str,
    ) -> str:
        """
        Ask the LLM to generate a specific cross-domain echo.
        Falls back to template if LLM fails.
        """
        prompt = (
            f"A memory from the '{source_domain}' domain:\n"
            f"  \"{content[:300]}\"\n\n"
            f"In one sentence, describe the echo of this memory in the "
            f"'{target_domain}' domain. Be concrete and specific. "
            f"Template hint: {template}"
        )
        try:
            result = self._llm_fn(prompt)
            if result and len(result.strip()) > 10:
                return result.strip()
        except Exception:
            pass
        return template

    # ── Retrieval scoring ────────────────────────────────────────────────

    def _resonance_score(
        self,
        query_tokens: set[str],
        memory: MemoryHologram,
        domains: list[str] | None,
    ) -> float:
        """
        Compute how strongly a memory resonates with a query.

        Score components:
        - Direct content overlap (weight: 1.0)
        - Cross-domain echo overlap for target domains (weight: 0.5 each)
        - Phi boost: high-Φ memories are slightly preferred (weight: 0.1)
        - Recency decay: older memories score slightly less (weight: 0.05)
        """
        score = 0.0

        # Direct content match
        content_tokens = set(self._tokenize(memory.content))
        score += self._token_overlap(query_tokens, content_tokens) * 1.0

        # Cross-domain echo match
        target_domains = domains if domains else _ALL_DOMAINS
        for domain in target_domains:
            if domain != memory.domain:
                echo = memory.echo_for(domain)
                echo_tokens = set(self._tokenize(echo))
                score += self._token_overlap(query_tokens, echo_tokens) * 0.5

        # Phi boost
        score += memory.phi * 0.1

        # Recency decay: memories older than 30 days lose 10% score
        age_days = (time.time() - memory.timestamp) / 86400
        if age_days > 30:
            score *= max(0.5, 1.0 - (age_days - 30) / 365 * 0.5)

        return score

